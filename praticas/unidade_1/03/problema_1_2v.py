codigo_dia = int(input('Dia da semana: '))

if (codigo_dia == 0):
    print('Domingo')
else:
    if (codigo_dia == 1):
        print('Segunda-feira')
    else:
        if (codigo_dia == 2):
            print('Terça-feira')
        else:
            if (codigo_dia == 3):
                print('Quarta-feira')
            else:
                if (codigo_dia == 4):
                    print('Quinta-feira')
                else:
                    if (codigo_dia == 5):
                        print('Sexta-feira')
                    else:
                        print('Sábado')

