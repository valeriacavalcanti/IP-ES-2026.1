codigo_dia = int(input('Dia da semana: '))

if (codigo_dia == 0):
    print('Domingo')
elif (codigo_dia == 1):
    print('Segunda-feira')
elif (codigo_dia == 2):
    print('Terça-feira')
elif (codigo_dia == 3):
    print('Quarta-feira')
elif (codigo_dia == 4):
    print('Quinta-feira')
elif (codigo_dia == 5):
    print('Sexta-feira')
else:
    print('Sábado')

