codigo_dia = int(input('Dia da semana: '))

if (codigo_dia == 0):
    dia = 'Domingo'
elif (codigo_dia == 1):
    dia = 'Segunda-feira'
elif (codigo_dia == 2):
    dia = 'Terça-feira'
elif (codigo_dia == 3):
    dia = 'Quarta-feira'
elif (codigo_dia == 4):
    dia = 'Quinta-feira'
elif (codigo_dia == 5):
    dia = 'Sexta-feira'
else:
    dia = 'Sábado'

print(dia)

