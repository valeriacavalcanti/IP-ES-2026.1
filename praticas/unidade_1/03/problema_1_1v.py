codigo_dia = int(input('Dia da semana: '))

if (codigo_dia == 0):
    print('Domingo')

if (codigo_dia == 1):
    print('Segunda-feira')

if (codigo_dia == 2):
    print('Terça-feira')

if (codigo_dia == 3):
    print('Quarta-feira')

if (codigo_dia == 4):
    print('Quinta-feira')

if (codigo_dia == 5):
    print('Sexta-feira')

if (codigo_dia == 6):
    print('Sábado')

